package nl.isld.databees;

public class Apiary {
	
	public int ID;
	public String geoLat, geoLng;
	
	public Apiary (int ID, String geoLat, String geoLng) {
		
	}
	
	public int getID() {
		return ID;		
	}
	
	public String getGeoLat() {
		return geoLat;		
	}
	
	public String getGeoLng() {
		return geoLng;		
	}
}
